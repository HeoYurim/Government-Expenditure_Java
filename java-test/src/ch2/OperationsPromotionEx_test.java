package ch2;

public class OperationsPromotionEx_test {

	public static void main(String[] args) {

		//byte 값 두개를 합친 값 출력
		byte byteNum = 20;
		byte byteNum2 = 30;
		
		int byteNumPlus = byteNum + byteNum2;
		System.out.println(byteNumPlus);
		
		//char 값 두개 합친 값 출력
		char charNum = 'B'; //66
		char charNum2 = 10; //10
		
		int charNumPlus = charNum + charNum2;
		System.out.println(charNumPlus);
		
		
		
	}

}
