package ch01;

public class RunStateExam {

	public static void main(String[] args) { //노란 줄은 아직 쓰이지 않았기 때문에 나오는 것

		int x = 1; //x 변수의 숫자 1을 넣는다.
		int y = 2; //y 변수의 숫자 2를 넣는다.
		int result = x + y; // result 변수에 x + y 값을 넣는다.
		
		System.out.println("1 + 2 = " + result); // 출력문 괄호 안쪽에 + 는 글자를 붙이는 연산자이다.
		
	}

}
